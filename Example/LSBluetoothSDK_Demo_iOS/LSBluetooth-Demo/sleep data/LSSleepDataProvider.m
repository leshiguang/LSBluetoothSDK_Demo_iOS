    long startUtc=1532169299;
    for(int index=0;index <3;index++){
        LSSleepData *item=[[LSSleepData alloc] initWithData:nil];
        item.deviceMac=@"C2870BAA87F3";
        item.timeOffset=300;
        item.utc=startUtc;
        item.status=[self getTestData:index];
        //update test time
        startUtc=startUtc+(item.status.count*300);
        [list addObject:item];
    }
    return list;
}


